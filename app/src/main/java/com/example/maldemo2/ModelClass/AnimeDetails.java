package com.example.maldemo2.ModelClass;

public class AnimeDetails {


    int mal_id;

    String url;

    String image_url;

    String title;

    Boolean airing;

    String synopsis;

    String type;

    int episodes;

    float score;

    String start_date;

    String end_date;

    int members;

    String rated;

    public int getMal_id() {
        return mal_id;
    }

    public String getUrl() {
        return url;
    }

    public String getImage_url() {
        return image_url;
    }

    public String getTitle() {
        return title;
    }

    public Boolean getAiring() {
        return airing;
    }

    public String getSynopsis() {
        return synopsis;
    }

    public String getType() {
        return type;
    }

    public int getEpisodes() {
        return episodes;
    }

    public float getScore() {
        return score;
    }

    public String getStart_date() {
        return start_date;
    }

    public String getEnd_date() {
        return end_date;
    }

    public int getMembers() {
        return members;
    }

    public String getRated() {
        return rated;
    }
}
